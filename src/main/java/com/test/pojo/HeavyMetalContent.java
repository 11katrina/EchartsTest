package com.test.pojo;


public class HeavyMetalContent {

  private long soilNumber;
  private double asC;
  private double cdC;
  private double hgC;
  private double znC;
  private double cuC;


  public long getSoilNumber() {
    return soilNumber;
  }

  public void setSoilNumber(long soilNumber) {
    this.soilNumber = soilNumber;
  }


  public double getAsC() {
    return asC;
  }

  public void setAsC(double asC) {
    this.asC = asC;
  }


  public double getCdC() {
    return cdC;
  }

  public void setCdC(double cdC) {
    this.cdC = cdC;
  }


  public double getHgC() {
    return hgC;
  }

  public void setHgC(double hgC) {
    this.hgC = hgC;
  }


  public double getZnC() {
    return znC;
  }

  public void setZnC(double znC) {
    this.znC = znC;
  }


  public double getCuC() {
    return cuC;
  }

  public void setCuC(double cuC) {
    this.cuC = cuC;
  }

}
